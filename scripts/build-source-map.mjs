#!/usr/bin/env node
/**
 * build-source-map — generates a slug→source map for the docs site.
 *
 * Why: registry components can't read fs (per CLAUDE.md mandate to keep them
 * portable for NPM extraction), but the docs detail page needs the verbatim
 * `demo.tsx` source string to render via <CodeBlock /> ("View Code" Dialog +
 * inline demo-source block). Doing the read at request time pulls fs into
 * every RSC render; the cleaner path is to generate a typed map at build /
 * dev start and import it as a normal module.
 *
 * Picks up:
 *   - src/registry/components/<category>/<slug>/demo.tsx        → map[slug].demo
 *   - src/registry/components/<category>/<slug>/examples/*.tsx  → map[slug].examples[exampleSlug]  (Phase 2)
 *
 * Emits:
 *   src/app/components/[slug]/_lib/source-map.generated.ts
 *
 * Wire-up: package.json `prebuild` + `predev` hooks. Adding a new component
 * requires a dev-server restart (the generator doesn't watch).
 *
 * Exit:
 *   0 — wrote the map
 *   1 — IO error
 */

import { readdir, readFile, mkdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = process.cwd();
const COMPONENTS_DIR = join(ROOT, "src", "registry", "components");
const OUT_DIR = join(ROOT, "src", "app", "components", "[slug]", "_lib");
const OUT_FILE = join(OUT_DIR, "source-map.generated.ts");

async function listSubdirs(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  return entries.filter((e) => e.isDirectory()).map((e) => e.name);
}

async function fileExists(path) {
  return existsSync(path);
}

async function listExampleFiles(slugDir) {
  const examplesDir = join(slugDir, "examples");
  if (!existsSync(examplesDir)) return [];
  const entries = await readdir(examplesDir, { withFileTypes: true });
  return entries
    .filter((e) => e.isFile() && e.name.endsWith(".tsx"))
    .map((e) => ({
      slug: e.name.replace(/\.tsx$/, ""),
      path: join(examplesDir, e.name),
    }));
}

function escapeForBacktickString(source) {
  // Backtick template strings need ` and ${ escaped. Newlines, quotes, etc.
  // pass through verbatim — that's the whole point of using a template here.
  return source.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

async function main() {
  if (!existsSync(COMPONENTS_DIR)) {
    console.error(`[build-source-map] not found: ${COMPONENTS_DIR}`);
    process.exit(1);
  }

  const map = {};
  const categories = await listSubdirs(COMPONENTS_DIR);
  let demoCount = 0;
  let exampleCount = 0;

  for (const category of categories) {
    if (category.startsWith("_")) continue; // skip _template, _shared, etc.
    const categoryDir = join(COMPONENTS_DIR, category);
    const slugs = await listSubdirs(categoryDir);

    for (const slug of slugs) {
      if (slug.startsWith("_")) continue;
      const slugDir = join(categoryDir, slug);
      const demoPath = join(slugDir, "demo.tsx");
      if (!(await fileExists(demoPath))) continue;

      const demoSource = await readFile(demoPath, "utf8");
      map[slug] = { demo: demoSource };
      demoCount++;

      const examples = await listExampleFiles(slugDir);
      if (examples.length > 0) {
        map[slug].examples = {};
        for (const ex of examples) {
          map[slug].examples[ex.slug] = await readFile(ex.path, "utf8");
          exampleCount++;
        }
      }
    }
  }

  await mkdir(OUT_DIR, { recursive: true });

  const slugs = Object.keys(map).sort();
  const banner = [
    "// AUTO-GENERATED by scripts/build-source-map.mjs — do not edit by hand.",
    "// Regenerated on every `prebuild` and `predev`. Source: demo.tsx + examples/*.tsx",
    "// files under src/registry/components/<category>/<slug>/.",
    "",
    "export type SourceMapEntry = {",
    "  demo: string;",
    "  examples?: Record<string, string>;",
    "};",
    "",
    "export const sourceMap: Record<string, SourceMapEntry> = {",
  ].join("\n");

  const body = slugs
    .map((slug) => {
      const entry = map[slug];
      const demoLit = `\`${escapeForBacktickString(entry.demo)}\``;
      const lines = [`  ${JSON.stringify(slug)}: {`, `    demo: ${demoLit},`];
      if (entry.examples) {
        lines.push("    examples: {");
        for (const exSlug of Object.keys(entry.examples).sort()) {
          const exLit = `\`${escapeForBacktickString(entry.examples[exSlug])}\``;
          lines.push(`      ${JSON.stringify(exSlug)}: ${exLit},`);
        }
        lines.push("    },");
      }
      lines.push("  },");
      return lines.join("\n");
    })
    .join("\n");

  const footer = [
    "};",
    "",
    "export function getDemoSource(slug: string): string | undefined {",
    "  return sourceMap[slug]?.demo;",
    "}",
    "",
    "export function getExampleSource(",
    "  slug: string,",
    "  exampleSlug: string,",
    "): string | undefined {",
    "  return sourceMap[slug]?.examples?.[exampleSlug];",
    "}",
    "",
  ].join("\n");

  const out = [banner, body, footer].join("\n");
  await writeFile(OUT_FILE, out, "utf8");

  const rel = relative(ROOT, OUT_FILE).split(/[\\/]/).join("/");
  console.log(
    `[build-source-map] wrote ${rel} — ${demoCount} demos, ${exampleCount} examples`,
  );
}

main().catch((err) => {
  console.error("[build-source-map] failed:", err);
  process.exit(1);
});
